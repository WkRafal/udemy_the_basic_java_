package com.exemple.basic.data;

public class MathRoundCeilFloor {
    public static void main(String[] args) {

        System.out.println(Math.round(5.51));
        System.out.println(Math.round(5.49));

        System.out.println((int) 5.65);

        System.out.println(Math.ceil(5.45));

        System.out.println(Math.floor(6.89));
    }
}
